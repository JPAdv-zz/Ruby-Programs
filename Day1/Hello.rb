# Different ways of printing "Hello, World!" in Ruby
# 
# Author: Jose Padilla

# Simple print "Hello, World!"
puts "Hello, World!"

# Ruby's way of creating variables and calling them.
# Ruby double quotes are evaluated for any included variables.
varname = 'Hello, World!'
puts "#{varname}"

# Paranthesis can also be used in Ruby for clarity.
puts ('Hello, World!')

# Another way of printing "Hello, World!" similar to most languages.
print "Hello, World!\n"
